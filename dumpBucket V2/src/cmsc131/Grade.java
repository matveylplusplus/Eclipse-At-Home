package cmsc131;

import java.util.Scanner;

public class Grade 
{
	public static void main(String[] Args)
	{
		int gr;
		String letter = "Unknown";
		
		System.out.print("Input numerical grade: ");
		Scanner ioo = new Scanner(System.in);
		gr = ioo.nextInt();
		System.out.println();
		
		if(gr >= 90)
			letter = "A";
		else if (gr >= 80)
			letter = "B";
		else if (gr >= 70)
			letter = "C";
		else if (gr >= 60)
			letter = "D";
		else
			letter = "F";
		System.out.println("Your grade is " + letter + "!");
	}
}
